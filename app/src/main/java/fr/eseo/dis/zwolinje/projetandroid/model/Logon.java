package fr.eseo.dis.zwolinje.projetandroid.model;

public class Logon {

    String result;
    String api;
    String token;

    public Logon(){

    }

    public String getResult(){
        return this.result;
    }

    public void setResult(String result){
        this.result = result;
    }

    public String getApi(){
        return this.api;
    }

    public void setApi(String api){
        this.api = api;
    }

    public String getToken(){
        return this.token;
    }

    public void setToken(String token){
        this.token = token;
    }


}
